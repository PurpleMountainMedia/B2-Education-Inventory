<template lang="html">
    <!-- Errors -->
    <el-alert v-show="showErrors"
              :title="errors.message"
              class="form_error mt-sm mb-sm"
              type="error"
              show-icon>
        <template>
            <template v-for="errors in errors.errors">
                <p v-for="message in errors" class="el-alert__description">- {{ message }}</p>
            </template>
        </template>
    </el-alert>
</template>

<script>
export default {
    name: 'B2Errors',

    props: {
        errors: {
            type: Object,
            required: true,
        }
    },

    computed: {

        /**
         * Whether or not to show the errors.
         *
         * @return boolean
         */
        showErrors()
        {
            return Object.keys(this.errors).length > 0;
        }
    }
}
</script>

